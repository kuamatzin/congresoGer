<h1 class="text-center">ACERCA DE LA RIDIT</h1>

<div class="card mt-5">
    <div class="card-body">
        <p class="card-text">La Red de Investigación y Docencia en Innovación Tecnológica RIDIT es una iniciativa académica que surgió en el año 2002, que tiene como objetivo impulsar la colaboración en el estudio y la formación de investigadores especializados en la innovación y el cambio técnico.</p>
        <p class="card-text">Actualmente está compuesta por 20 nodos, que pertenecen a 9 instituciones mexicanas, dos españolas,  y una más en cada los siguientes países: Cuba, Costa Rica,  Colombia y Estados Unidos. Si bien se trata de una red académica, también busca interactuar con diversos agentes, públicos y privados. Además de tener sus congresos nacionales e internacionales como espacios de intercambio y diálogo, también se ha impulsado la realización de actividades docentes.</p>
        <p class="card-text">Hasta el año 2017, se han realizado 12 Congresos Nacionales y 9 Internacionales. Además de impartirse en colaboración varios cursos y seminarios, así como libros derivados de dichos Congresos.</p>
